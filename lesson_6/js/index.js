/*Функция принимает какой-то текст и массив спам-слов. Функция должна вернуть true, 
если текст содержит слова из списка spamWords или false если не содержит. 
Функция не должна быть чувствительна к регистру 
(если слово в списке спама "новинка", а в тексте "НоВиНкА" - то это спам)*/

function isSpam(text, spamWords)
{
      return ~text.toUpperCase().indexOf((spamWords.toUpperCase())) ? true: false;
}

