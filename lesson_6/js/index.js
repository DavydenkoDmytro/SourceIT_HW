/*4. Реализуйте функцию, которая обрезает длинный текст по следующему принципу: 
если количество символов этого текста больше заданного в аргументе n, то в переменную result 
запишем первые n символов строки str и добавим в конец троеточие '...'. 
В противном случае в переменную result запишем содержимое переменной str. Функция должна вернуть result.*/

function cutText(str, n)
{
    return str.length > n ? str.substring(0, n)+"..." : str;
}

let result = cutText("первые n символов строки str и добавим в конец троеточие", 10);


