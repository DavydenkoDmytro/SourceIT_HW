/*2. Функция принимает массив имен. Имена могут быть в любом виде: пЕтя, Артём, катя, МаШа.
 Функция должна вернуть строку с нормализованными именами, разделенными запятой и пробелом: 
 "Петя, Артём, Катя, Маша"*/

function getCapitalizedNameString(names)
{
    let string = [];
    names.forEach((item) => {
        item = item.toLowerCase();
        item = item.replace(item[0],item[0].toUpperCase());
        string.push(item);
      });
    return string.join(', ');
}

let name = getCapitalizedNameString(["пЕтя", "Артём", "катя", "МаШа"]);

